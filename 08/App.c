void main() {
    int tick = 0;

    short hour;
    short min;
    short sec;

    printf("Please input a tick between 0~86399: \n");
    scanf_s("%d", &tick);

    hour = CalcHour(tick);
    min = CalcMin(tick);
    sec = CalcSec(tick);

    printf("Current time: %02d-%02d-%02d\n", hour, min, sec);
    return;
}
