static void InitSoftware(void);
static void __stdcall TimeProc(UINT uTimerID, UINT uMsg, DWORD_PTR dwUser,
                               DWORD_PTR dw1, DWORD_PTR dw2);    //定时器回调函数

static void Proc2msTask(void);      // 声明一个2ms执行一次的函数
static void Proc1SecTask(void);     // 声明一个1sec执行一次的函数

/*********************************************************************************************************
*                                              内部函数实现
*********************************************************************************************************/
/*********************************************************************************************************
* 函数名称: 
* 函数功能: 
* 输入参数: 
* 输出参数: 
* 返 回 值: 
* 创建日期: 
* 注    意: 
*********************************************************************************************************/
static void InitSoftware(void) {
    InitRunClock();

    SetTimeVal(TIME_VAL_HOUR, 23);
    SetTimeVal(TIME_VAL_MIN, 59);
    SetTimeVal(TIME_VAL_SEC, 49);
}

/*********************************************************************************************************
* 函数名称: TimeProc
* 函数功能:
* 输入参数:
* 输出参数:
* 返 回 值:
* 创建日期:
* 注    意:
*********************************************************************************************************/
static void __stdcall TimeProc(UINT uTimerID, UINT uMsg, DWORD_PTR dwUser,
                               DWORD_PTR dw1, DWORD_PTR dw2) {
    Proc2msTask();
}

/*********************************************************************************************************
* 函数名称:
* 函数功能:
* 输入参数:
* 输出参数:
* 返 回 值:
* 创建日期:
* 注    意:
*********************************************************************************************************/
static void Proc2msTask(void) {
    static short s_iCnt500 = 0;

    RunClockPer2Ms();

    if(s_iCnt500 >= 499) {
        Proc1SecTask();
        s_iCnt500 = 0;
    }
    else {
        s_iCnt500++;
    }
}

/*********************************************************************************************************
* 函数名称:
* 函数功能:
* 输入参数:
* 输出参数:
* 返 回 值:
* 创建日期:
* 注    意:
*********************************************************************************************************/
static void Proc1SecTask(void) {
    DispTime(GetTimeVal(TIME_VAL_HOUR), GetTimeVal(TIME_VAL_MIN), GetTimeVal(TIME_VAL_SEC));
}
