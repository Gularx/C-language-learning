int back(int arr[]){
    int maxn = arr[0];
    int maxptr=0;
    for(int i=1;i<10;i++){
        if(*(arr+i)>maxn){
            maxn = *(arr+1);
            maxptr = i;
        }
    }
    return maxptr;
}

void findMaxIndex(int* arr, int *maxIndex, int size) {
    int maxVal = *arr;
    *maxIndex = 0;
    
    for (int i = 1; i < size; i++) {
        if (arr[i] > maxVal) {
            maxVal = arr[i];
            *maxIndex = i;
        }
    }
}

