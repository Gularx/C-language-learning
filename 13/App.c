static void Proc2msTask(void) {
    static  i16 s_iCnt500 = 0;

    if(s_iCnt500 >= 499) {
        Proc1SecTask();
        s_iCnt500 = 0;
    }
    else {
        s_iCnt500++;
    }
}

/*********************************************************************************************************
* 函数名称:
* 函数功能:
* 输入参数:
* 输出参数:
* 返 回 值:
* 创建日期:
* 注    意:
*********************************************************************************************************/
static void Proc1SecTask(void) {
    static i16 s_iCnt = 0;

    s_iCnt++;

    if(s_iCnt == 5) {
        printf("\n");
        ProcHostCmd(0x70);
        ProcHostCmd(0x81);
        ProcHostCmd(0x80);
        ProcHostCmd(0x80);
        ProcHostCmd(0x80);
        ProcHostCmd(0x80);
        ProcHostCmd(0x80);
        ProcHostCmd(0x80);
        ProcHostCmd(0x80);
        ProcHostCmd(0xF1);
    }
    else if(s_iCnt == 10) {
        printf("\n");
        ProcHostCmd(0x71);
        ProcHostCmd(0x81);
        ProcHostCmd(0x80);
        ProcHostCmd(0x80);
        ProcHostCmd(0x80);
        ProcHostCmd(0x80);
        ProcHostCmd(0x80);
        ProcHostCmd(0x80);
        ProcHostCmd(0x80);
        ProcHostCmd(0xF2);
        s_iCnt = 0;
    }
}

